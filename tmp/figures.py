def gen_op_counts_qconv(n, h, w, o, kh, kw, c, residual=False):
    dotp = n * h * w * o * kh * kw * c
    rcnv = n * h * w * o
    rrsd = (n * h * w * o) if residual else 0
    return dotp + rcnv + rrsd

def gen_mem_counts_qconv(n, h, w, o, kh, kw, c, ih, iw, residual=False, per_channel=True):
    image = n * ih * iw * c
    kernel = o * kh * kw * c
    if c > 64:
      kernel *= (ih + 13) // 14
      kernel *= (iw + 13) // 14

    bias = o
    requant = 1
    if per_channel:
      requant = o
    if residual:
      requant += 1
    residual = (n * h * w * o) if residual else 0
    output = n * h * w * o
    return image + kernel + bias + requant + residual + output

